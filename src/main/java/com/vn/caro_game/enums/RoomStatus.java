package com.vn.caro_game.enums;

/**
 * Enum representing the status of a game room
 */
public enum RoomStatus {
    /**
     * Room is waiting for players to join
     */
    WAITING,
    
    /**
     * Game is currently in progress
     */
    PLAYING,
    
    /**
     * Game has finished
     */
    FINISHED
}
