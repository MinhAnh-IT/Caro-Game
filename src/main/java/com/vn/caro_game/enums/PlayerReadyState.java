package com.vn.caro_game.enums;

/**
 * Enum representing player ready state
 */
public enum PlayerReadyState {
    /**
     * Player is not ready to start
     */
    NOT_READY,
    
    /**
     * Player is ready to start
     */
    READY,
    
    /**
     * Player is currently in game
     */
    IN_GAME
}
