package com.vn.caro_game.enums;

/**
 * Enum representing the status of a friendship relationship
 */
public enum FriendStatus {
    /**
     * Friend request is pending approval
     */
    PENDING,
    
    /**
     * Friend request has been accepted
     */
    ACCEPTED,
    
    /**
     * User has been blocked
     */
    BLOCKED
}
